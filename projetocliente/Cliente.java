package victor.projetocliente;

public class Cliente {
	
	private String nome;
	private int cpf;
	private int telefone;
	private int idade;
	
	public int setIdade(int idade) {
		return idade;
	}
	public void getidade() {
		this.idade = idade;
	}
	public String setNome(String nome) {
		return nome;
	}
	public void getNome() {
		this.nome = nome;
	}
	public int setCPF(int cpf) {
		return cpf;
	}
	public void getCPF() {
		this.cpf = cpf;
	}
	public int setTel(int telefone) {
		return telefone;
	}
	public void getTel() {
		this.telefone = telefone;
	}
	

}
